class Book {
  constructor(
    id,
    categoryIds,
    title,
    type,
    rating,
    pages,
    imageUrl,
    ingredients,
    steps,
    isGlutenFree,
    isVegan,
    isVegetarian,
    isLactoseFree
  ) {
    this.id = id;
    this.categoryIds = categoryIds;
    this.title = title;
    this.type = type;
    this.rating = rating;
    this.pages = pages;

    this.imageUrl = imageUrl;
    this.ingredients = ingredients;
    this.steps = steps;
    this.isGlutenFree = isGlutenFree;
    this.isVegan = isVegan;
    this.isVegetarian = isVegetarian;
    this.isLactoseFree = isLactoseFree;
  }
}

export default Book;
